public class permohonanSukarelawan {
    
    private String idPermohonan;
    private String statusPengguna;
    private String butiranPermohonan;
    private String tarikhPermohonan;
    private String penerimaanPenolakan;
    private String terimaTolak;

    public String getIdPermohonan() {
        return idPermohonan;
    }

    public String getStatusPengguna() {
        return statusPengguna;
    }

    public String getButiranPermohonan() {
        return butiranPermohonan;
    }

    public String getTarikhPermohonan() {
        return tarikhPermohonan;
    }

    public String getPenerimaanPenolakan() {
        return penerimaanPenolakan;
    }

    public String getTerimaTolak() {
        return terimaTolak;
    }

    public void setIdPermohonan(String idPermohonan) {
        this.idPermohonan = idPermohonan;
    }

    public void setStatusPengguna(String statusPengguna) {
        this.statusPengguna = statusPengguna;
    }

    public void setButiranPermohonan(String butiranPermohonan) {
        this.butiranPermohonan = butiranPermohonan;
    }

    public void setTarikhPermohonan(String tarikhPermohonan) {
        this.tarikhPermohonan = tarikhPermohonan;
    }

    public void setPenerimaanPenolakan(String penerimaanPenolakan) {
        this.penerimaanPenolakan = penerimaanPenolakan;
    }

    public void setTerimaTolak(String terimaTolak) {
        this.terimaTolak = terimaTolak;
    }
}
